package ru.avklimenko.hedgehog;

public class ClassForMap {
    private Integer a;
    protected Float b;
    public String c;

    public void setA(Integer a) {
        this.a = a;
    }

    public void setB(Float b) {
        this.b = b;
    }

    public Integer getA() {
        return a;
    }

    public Float getB() {
        return b;
    }

    public String getC() {
        return c;
    }
}